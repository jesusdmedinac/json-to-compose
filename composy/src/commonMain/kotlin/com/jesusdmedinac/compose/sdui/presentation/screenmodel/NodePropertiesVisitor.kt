    fun visit(props: NodeProperties.ScaffoldProps): T = visitDefault(props)
    fun visit(props: NodeProperties.SearchBarProps): T = visitDefault(props)
    fun visit(props: NodeProperties.SegmentedButtonProps): T = visitDefault(props)
    fun visit(props: NodeProperties.SegmentedButtonRowProps): T = visitDefault(props)
    fun visit(props: NodeProperties.SliderProps): T = visitDefault(props)
    fun visit(props: NodeProperties.SnackbarHostProps): T = visitDefault(props)
    fun visit(props: NodeProperties.SpacerProps): T = visitDefault(props)
    fun visit(props: NodeProperties.SurfaceProps): T = visitDefault(props)
    fun visit(props: NodeProperties.SwitchProps): T = visitDefault(props)
    fun visit(props: NodeProperties.TabProps): T = visitDefault(props)
    fun visit(props: NodeProperties.TabRowProps): T = visitDefault(props)
    fun visit(props: NodeProperties.TextFieldProps): T = visitDefault(props)
    fun visit(props: NodeProperties.TextProps): T = visitDefault(props)
    fun visit(props: NodeProperties.TimePickerProps): T = visitDefault(props)
    fun visit(props: NodeProperties.TopAppBarProps): T = visitDefault(props)
    fun visitDefault(props: NodeProperties): T
}

