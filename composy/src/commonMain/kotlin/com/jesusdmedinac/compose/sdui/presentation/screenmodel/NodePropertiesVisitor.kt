        is NodeProperties.ChipProps -> visitor.visit(this)
        is NodeProperties.ColumnProps -> visitor.visit(this)
        is NodeProperties.CustomProps -> visitor.visit(this)
        is NodeProperties.DatePickerProps -> visitor.visit(this)
        is NodeProperties.DividerProps -> visitor.visit(this)
        is NodeProperties.ExtendedFabProps -> visitor.visit(this)
