        is NodeProperties.BadgedBoxProps -> visitor.visit(this)
        is NodeProperties.BottomBarProps -> visitor.visit(this)
        is NodeProperties.BottomNavigationItemProps -> visitor.visit(this)
        is NodeProperties.BoxProps -> visitor.visit(this)
        is NodeProperties.ButtonProps -> visitor.visit(this)
        is NodeProperties.CardProps -> visitor.visit(this)
