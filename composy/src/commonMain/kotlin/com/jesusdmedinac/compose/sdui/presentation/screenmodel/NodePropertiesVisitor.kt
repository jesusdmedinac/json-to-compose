    fun visit(props: NodeProperties.DatePickerProps): T = visitDefault(props)
    fun visit(props: NodeProperties.DividerProps): T = visitDefault(props)
    fun visit(props: NodeProperties.ExtendedFabProps): T = visitDefault(props)
