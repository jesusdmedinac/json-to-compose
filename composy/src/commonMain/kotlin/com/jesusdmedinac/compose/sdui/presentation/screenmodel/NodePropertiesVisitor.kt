        is NodeProperties.PlainTooltipProps -> visitor.visit(this)
        is NodeProperties.ProgressIndicatorProps -> visitor.visit(this)
        is NodeProperties.RadioButtonProps -> visitor.visit(this)
        is NodeProperties.RichTooltipProps -> visitor.visit(this)
        is NodeProperties.RowProps -> visitor.visit(this)
        is NodeProperties.ScaffoldProps -> visitor.visit(this)
        is NodeProperties.SearchBarProps -> visitor.visit(this)
        is NodeProperties.SegmentedButtonProps -> visitor.visit(this)
        is NodeProperties.SegmentedButtonRowProps -> visitor.visit(this)
