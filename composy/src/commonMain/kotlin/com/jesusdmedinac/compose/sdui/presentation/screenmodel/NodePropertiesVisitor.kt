    fun visit(props: NodeProperties.BadgeProps): T = visitDefault(props)
    fun visit(props: NodeProperties.BadgedBoxProps): T = visitDefault(props)
    fun visit(props: NodeProperties.BottomBarProps): T = visitDefault(props)
    fun visit(props: NodeProperties.BottomNavigationItemProps): T = visitDefault(props)
    fun visit(props: NodeProperties.BoxProps): T = visitDefault(props)
    fun visit(props: NodeProperties.ButtonProps): T = visitDefault(props)
    fun visit(props: NodeProperties.CardProps): T = visitDefault(props)
    fun visit(props: NodeProperties.CheckboxProps): T = visitDefault(props)
    fun visit(props: NodeProperties.ChipProps): T = visitDefault(props)
    fun visit(props: NodeProperties.ColumnProps): T = visitDefault(props)
